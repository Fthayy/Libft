int ft_toupper(int c)
{
	if ( c >= 97 && c <= 122)
		c -= 32;
	return c;
}

#include <stdio.h>
#include <ctype.h>
int main() {
    char c;

    c = 'm';
    printf("%c -> %c", c, ft_toupper(c));
}
