int	ft_memcmp(const void *s1, const void *s2, int n)
{
	int			x;
	const char	*str1;
	const char	*str2;

	str1 = s1;
	x = 0;
	str2 = s2;
	while (str1[x] != '\0' && str2[x] != '\0' && str1[x] == str2[x] && x < n)
		x++;
	return (str1[x] - str2[x]);
}
